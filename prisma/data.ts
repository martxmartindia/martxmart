// Seed Categories
for (const category of [...foodProcessingCategories, ...manufacturingCategories, ...serviceCategories, ...tradingCategories]) {

  // Create or update Category
  const createdCategory = await prisma.plantCategory.upsert({
    where: { slug: category.slug },
    update: {},
    create: {
      name: category.name,
      slug: category.slug,
      description: category.description,
      imageUrl: `https://martxmart.com/logo.png/300x200?text=${encodeURIComponent(category.name)}`,
    },
  });

  // Seed Plants under this category
  const plantData = category.plants.map((plant) => ({
    name: plant.name,
    slug: plant.slug,
    description: plant.description,
    longDescription: `${plant.description} with advanced technology.`,
    plantCategoryId: createdCategory.id,
    imageUrl: `https://martxmart.com/logo1.png/150?text=${encodeURIComponent(plant.name)}`,
    capacity: plant.capacity,
    powerConsumption: plant.powerConsumption,
    price: plant.price,
    specifications: plant.specifications,
  }));

  await prisma.plant.createMany({
    data: plantData,
    skipDuplicates: true,
  });
}

